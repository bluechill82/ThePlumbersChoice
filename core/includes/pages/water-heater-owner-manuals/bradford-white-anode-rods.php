<h1>Bradford White</h1>
<p>A combination sacrificial anode rod/hot water outlet nipple has been installed to extend tank life. The anode rod should be inspected periodically (every 2 years) and replaced when necessary to prolong tank life. Water conditions in your area will influence the time interval for inspection and replacement of the anode rod. Contact the plumbing professional who installed the water heater or the manufacturer listed on the rating plate for anode replacement information. The use of a water softener may increase the speed of anode consumption. More frequent inspection of the anode is needed when using softened (or phosphate treated) water.</p>
