						<h1 class="p3h1">How to Replace the Anode Rod</h1>
						<p class="p3-p1">CAUTION: Draining your water heater for this procedure may put you at risk of being scalded by hot water. Please be careful when working on your water heater.
						</p>
							<ol class="p3-ol">
								<li>Turn the power OFF to the water heater at the circuit breaker (electric heater) or main gas line (gas heater).</li>	
								<li>Fasten a length of garden hose to the drain valve at the bottom of the heater. Put the other end of the garden hose in the nearest floor drain or snake it outside the home.</li>
								<li>Close the shut off valve at the cold water inlet line.</li><br /><br />
								<li>Open the temperature and pressure relief valve at the top of the heater. This will relieve pressure inside the tank. Now open the drain valve and allow about a gallon of the water to drain. You will probably notice some small white particles (called scale or sediment) during the early stages of the flow.<div id="alert">CAUTION: THE WATER WILL BE HOT...........BE SURE NO ONE IS NEAR THE DRAIN HOSE OR THEY COULD BE SCALDED.</div></li>
								<li>After about a gallon of water, close the drain valve and remove the hose. Leave the temperature and pressure relief valve at the top of the heater open for the time being.</li>
								<li>Remove the old anode rod with a 1 and 1/16 inch six-sided wrench or socket. Do NOT use a 12-point wrench or socket or you will strip the head of the anode. Get at least a 24-inch cheater bar. I keep a piece of 3/4 inch black iron pipe in the garage just for this purpose. Place the cheater pipe over the socket handle. Have someone hold the water heater while you slowly break the seal. Once broken, the anode removes quite easily.</li>
								<li>Replace with a new anode rod. It is a 3/4-inch National Pipe Thread (NPT) part. You may find one at any plumbing house or home store. Use pipe sealer or Teflon tape to seal the threads. Snug it down tight. Remember the water heater builds up 150 PSI of pressure inside the tank.</li>	
								<li>Open a hot water faucet somewhere in the home. Open the shut off valve at the cold water inlet line. You will hear the heater start to fill.</li>
								<li>When you have a steady stream of water from the temperature and pressure valve tube, close the valve; when you have a steady stream of water from the hot water faucet, turn the faucet off.</li>
								<li>Turn the power ON to the water heater at the circuit breaker (electric heater) or main gas line (gas heater; relight pilot if required). Allow the water heater to recover. Check the anode rod one more time to make sure it is tight and there are no leaks.</li>
							</ol>
