				<h1>State</h1>
				<h1 style="font-size:20px;">Anode Rod Inspection</h1>
				<p>The anode rod is used to protect the tank from corrosion. Most hot water tanks are equipped with an anode rod. The submerged rod sacrifices itself to protect the tank. Instead of corroding the tank, water ions attack and eat away the anode rod. This does not affect the water&rsquo;s taste or color.</p>
				<p>The rod must be maintained to keep the tank in operating condition. Anode deterioration depends on water conductivity, not necessarily water condition. A corroded or pitted anode rod indicates high water conductivity and should be checked and/or replaced more often than an anode rod that appears to be intact. Replacement of a depleted anode rod can extend the life of your water heater. Inspection should be conducted by a qualified technician, and at a minimum should be checked annually after the warranty period.</p>
