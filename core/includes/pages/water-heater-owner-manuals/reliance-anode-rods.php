				<h1>Reliance</h1>
				<p>The anode rod should be removed from the water heater tank every 3 years for inspection. The following are typical (but not all) signs of a depleted anode rod:</p>
				<ul>
					<li>The majority of the rods diameter is less than 3/8&rdquo;.</li>
					<li>Significant sections of the support wire (approx. 1/3 or more of the anode rod&rsquo;s length) are visible. If the anode rod shows signs of either or both it should be replaced.</li>
				</ul>
				</br>
				<span id="reliance-note-1"class="note">	
				NOTE: Whether reinstalling or replacing the anode rod, check for any leaks and immediately correct if found.
				</span>
