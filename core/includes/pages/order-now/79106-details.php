	<p id="art-3-text" itemprop="description">
		This is the Hex Plug model of the Blue Lightning Magnesium Flexible Anode Rod combined with the Drainage Kit. This model has a length of 42 inches, a diameter of .75 inches, and 3/4-14 NPT thread size (This sizing should be standard for the bulk of residential water heaters). The rod is broken down into four separate segments. Each segment is Magnesium on a steal rod core. The segments are attached via copper couplers and 301 stainless steel braided wire. <br />
		<br />
		The Drainage kit comes with a Full Port Brass Valve that provides a 3/4 inch connection compatible with your average garden hose. This provides a method for easily draining your water heater annually to reduce sediment buildup. This valve also provides an opening large enough for a long narrow object to breakup any hardened material that has collected at the bottom of the tank. The Full Port Valve comes with a water tight cap so that you can easily secure the connection when not in use. <br />
		<br />
		Together these items can significantly extend the life span of your water heater by preventing damage to the interior of the tank. Our innovatively designed Blue Lightning Magnesium Flexible Rod, also known as a sacrificial rod, is designed to attract the corrosive properties in your water heater tank and break down before the steel interior of the water heater through a process called electrolysis. In many cases, particularly when an aluminum anode rod was used previously, the rod slowly deteriorates and falls to the bottom of the water heater creating a destructive sludge. If this sludge is left unattended it can cause the drainage valve at the bottom of the water heater to become badly clogged and destroy the integrity of the appliance. <br />
		<br />
		Any sludge should be broken up and flushed from the bottom of the tank annually along with an inspection of your Blue Lightning Magnesium Anode Rod. In most cases rod replacement should not take place for the first two years depending on the qualities of the water. </p>
